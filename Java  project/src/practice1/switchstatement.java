package practice1;

public class switchstatement {

	public static void main(String[] args) {

		int a = 12;
		switch (a) {
		case 2:
			System.out.println("even");
			break;
		case 4:
			System.out.println("even");
			break;
		case 6:
			System.out.println("even");
			break;
		case 8:
			System.out.println("even");
			break;
		case 10:
			System.out.println("even");
			break;
		case 12:
			System.out.println("even");
			break;
		default:
			System.out.println("odd");
			break;
		}

		int c = 20;
		switch (c) {
		case 10:
			System.out.println("even");
			break;
		case 20:
			System.out.println("even");
			break;
		case 30:
			System.out.println("even");
			break;
		case 40:
			System.out.println("even");
			break;
		case 50:
			System.out.println("even");
			break;
		default:
			System.out.println("odd");
			break;
		}
	}

}
